<div class="row">

<br><h2>TOBACCO</h2> - All Tobacco products are strictly prohibited (Smoking, Chewing, Pan / Pan Masala) in the Hospital premises.
<br>
<h2>SERVICES</h2> – Services of Medical, Nursing, Housekeeping and Maintenance staffs are available round the clock. In case of any difficulty contact your Nursing Station or Public Relation Officer of your floor / Doctor on Duty / AMS.
<br>
<h2>FOOD AND BEVERAGES</h2> - No Outside food or beverages are allowed in the hospital premises. Patient will be served therapeutic diet as per doctor’s advice in their respective room. Attendants will be served in the Dining Hall /Cafeteria. (Except bed tea on demand). No food / beverages are allowed for visitors in the room.
<br><h2>VISITING HOURS</h2> - Visiting Hours should be strictly adhered to. All patient coming to the hospital, have very low resistance to fight infection. Visitors can be source of infection to them. To avoid inconvenience to the patient, one visitor at a time will be allowed in the Wards between 5:00 – 7:00 pm (summer) and 4:00 – 6:00 pm (winter) daily. In ICU, Morning visiting hours will be from 10:30 – 11:00 am and Evening from 5:00 – 5:30 pm.
<br><h2>ATTENDANTS</h2> – Only one attendant is allowed with all categories of patients for the reason given above. Extra attendant is allowed in case of sick / critical patients on the discretion of NS / MS. A mattress with bedding will be provided for the attendant from 8:00 pm – 6:00 am only, on payment of charges as per prevalent rates.
<br><h2>OT/HDU/ICU TRANSFER</h2> – The attendants have to vacate the room whenever the patient is shifted to any critical care area or for a surgery / procedure. However, attendants have a choice of retaining the room (subjected to availability) while patient is in intensive / critical care area. Additional charges for the retained room shall be applicable on daily basis. Attendants can also use the facility for night stay (one attendant per patient) located on 1st Floor, Block – C, on additional charges.
<br><h2>SCHEDULE OF CHARGES</h2> – Hospital charges as already explained and are not negotiable. For any emergency admission certain advance amount is to be deposited as per the prevalent rates for different category of rooms at the time of admission. For planned admission 100% deposit to be made as per the Estimated Cost of Treatment given. An Estimate and the final bill charges may vary because of change in the medical treatment plan depending on patient’s condition during hospital stay. Each patient needs to maintain a minimum deposit of Rs. 10,000/- Patients should ensure sufficient advance deposit and timely deposit of bill amount on daily basis after receiving provisional bills given to all admitted patients to avoid inconvenience at the time of final billing and patient’s discharge.
<br><h2>ADMISSION ADVANCE</h2> – Admission advance must be paid at the time of admission to cover the expenses for surgery / treatment. Personal cheques are not accepted.
<br><h2>LEAVE PERMISSIONv</h2> – Leave permission for admitted panel patients. Leave permission to be availed from concerned office. Management is no-way responsible and shall not allow them leave during admission period except under special circumstances.
<br><h2>SETTLEMENT OF BILLS </h2>– Settlement of bills must be done on presentation of the bills. Pending payments as per the due slip should be cleared immediately. Please get receipt for all payments made.
<br><h2>DISCHARGE</h2> – Check out time is 12:00 Noon. Patients discharged between 12 Noon to 6 M will be charged ½ day’s room rent. Please clear your bills before 12 noon and 6 PM.
<br><h2>PATIENT’S BELONGINGS </h2>– The management will not in any way whatsoever, be responsible for any loss or damage to the patient’s belongings or any other property from either the hospital room, locker or any other part of the hospital, including theft or pilferage.
<br><h2>AIR CONDITIONING </h2>– Please ensure that all the windows / doors are closed for effective Air – conditioning.
<br><h2>WASHING OF CLOTHES</h2> – Washing of clothes is strictly prohibited inside the hospital. Out station patients can give their clothes to the Dhobi for washing.
<br><h2>TIPS / BAKSHISH</h2> - Please do not offer any Bakshish / tip to any of the hospital employee.
<br><h2>YOUR RESPONSE</h2> – For evaluation of Hospital services, you are requested to fill up the Patient’s Response Form provided to you. Your suggestion will help us to serve you better.
<br><h2>CHANGE OF ROOM </h2>- The Management shall have the right to change the room allotted to the patient at any time, without assigning any reason thereof and without any previous notice, or to shift the patient to any other suitable accommodation.
<br><h2>VIOLENCE </h2>- Violence against medical, paramedical, other hospital staff and damage to hospital property are punishable offences.
